        private SecurityErrorType ValidateRequest()
        {
            string strToSign;
            
            if (HttpContext.Request.Method.Equals("POST"))
            {
                HttpContext.Request.EnableRewind();
                HttpContext.Request.Body.Position = 0;
                using (StreamReader reader = new StreamReader(HttpContext.Request.Body, Encoding.UTF8, true, 1024, true))
                {
                    strToSign = $"{HttpContext.Request.Headers["Lykke-ApiKey"]}{reader.ReadToEnd()}";
                }
                HttpContext.Request.Body.Position = 0;
            }
            else
            {
                strToSign = $"{HttpContext.Request.Headers["Lykke-ApiKey"]}{HttpContext.Request.Scheme}://{HttpContext.Request.Host}{HttpContext.Request.Path}{HttpContext.Request.QueryString}";
            }

            return _securityHelper.CheckRequest(strToSign, HttpContext.Request.Headers["Lykke-Merchant-Id"], HttpContext.Request.Headers["Lykke-Merchant-Sign"]);
        }


        // POST api/rates/assetPair
        [HttpGet("{assetPair}")]
        public IActionResult Get(string assetPair)
        {
            var isValude = ValidateRequest();
            if (isValude != SecurityErrorType.Ok)
            {
                return Json(new RatesModel
                {
                    Result = isValude
                });
            }

            var sources = JsonConvert.DeserializeObject<List<dynamic>>(dummyJson);

            var result = new RateModel
            {
                Result = isValude
            };

            var rates = new List<Rate>();
            foreach (var source in sources)
            {
                var elem = rates.FirstOrDefault(s => s.AssetPair == source.AssetPair.ToString());
                if (elem == null)
                {
                    elem = new Rate(source);
                    rates.Add(elem);
                }
                else
                {
                    elem.FillRate(source);
                }
            }

            result.Rate = rates.FirstOrDefault(r => r.AssetPair.EndsWith(assetPair));
            return Json(result);
        }

        // POST api/rates
